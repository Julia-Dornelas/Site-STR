            ▪️ Em 1988, com a nova Constituição, conquistamos a aposentadoria aos 60 anos para homens e aos 55 para as mulheres rurais no valor de um salário mínimo, além do salário maternidade, pensão por morte e auxílio doença e equiparação ao trabalhador urbano, com os mesmos direitos.<BR><BR>
