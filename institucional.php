            ▪️ PAA -Programa de Aquisição de Alimento – Criado em 2003, através de nossas reivindicações e coordenado pela CONAB (Companhia Nacional de Abastecimento), com o objetivo de adquirir produtos agropecuários produzidos pelos agricultores familiares e assentados da Reforma Agrária, na garantia de Segurança Alimentar e geração de emprego e renda. <BR><BR>
            ▪️ PNAE - Programa Nacional de Alimentação Escolar: garantia de segurança alimentar e nutricional nas escolas, melhorando a qualidade de vida e até do aprendizado dos alunos. Fortalece a agricultura Familiar e os Projetos de Assentamentos, em razão de Lei Federal que determina no mínimo de 30% (trinta por cento) dos alimentos devem ser oriundos da Agricultura Familiar. Com isso, gera trabalho e renda no campo. <BR><BR>
            ▪️ PNHR – Programa Nacional de Habitação Rural: após a nossa luta e reivindicações do movimento sindical rural, assim como o crédito agrícola e assistência técnica, também conquistamos o direito do trabalhador rural ter uma moradia digna, tanto na construção como na reforma de sua casa.<BR><BR>
            ▪️ LPT – Programa Luz para Todos: Com nossa participação ativa no Comitê Gestor do Programa, fizemos com que a eletrificação rural realmente chegasse a todos. O movimento sindical é muito atuante com suas ações. <BR><BR>
            ▪️ PNCF - Programa Nacional de Crédito Fundiário: Possibilita o financiamento ao trabalhador, seja ele assalariado rural ou agricultor familiar, para ter sua propriedade rural, com recursos a juros mais baixos, carência, e vários anos para concluir o pagamento. <BR><BR>
